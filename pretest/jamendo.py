with open("jamendo_hippoprnb_candidates", "w", newline="", encoding="utf-8") as f:
    writer = csv.DictWriter(f, fieldnames=["id","name","artist","listens","url"])
    writer.writeheader()

    for url in urls:
        tid = url.strip().split("/")[-1]
        try:
            r = requests.get("https://api.jamendo.com/v3.0/tracks", params={
                "client_id": CLIENT_ID,
                "id": tid,
                "include": "stats",
                "format": "json"
            })
            data = r.json()["results"]
            if data:
                row = {
                    "id": tid,
                    "name": data[0]["name"],
                    "artist": data[0]["artist_name"],
                    "listens": data[0]["stats"]["rate_listened_total"],
                    "url": url
                }
                writer.writerow(row)
                f.flush()  # scrive subito su disco
                print(f"{data[0]['name']} | {data[0]['artist_name']} | listens: {data[0]['stats']['rate_listened_total']}")
            else:
                print(f"Nessun risultato per ID {tid}")
        except Exception as e:
            print(f"Errore su {tid}: {e} — salto")
        
        time.sleep(0.5)  # più lento per evitare rate limit

